package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=1;
        switch(jalanJalan){
            case 1:
                System.out.println("Urang bungas");
                break;
            case 2:
                System.out.println("Urang Gagah");
                break;
            case 3:
                System.out.println("Urang Cantik");
                break;
            case 4:
                System.out.println("Kada bungas");
                break;
            default:
                System.out.println("mahluk halus");
                break;
        }
    }
}
